<script>
import { PerfectScrollbar } from "vue2-perfect-scrollbar";
import "vue2-perfect-scrollbar/dist/vue2-perfect-scrollbar.css";

export default {
  name: "Meetingchat",

  components: {
    PerfectScrollbar,
  },

  data() {
    return {};
  },

  mounted() {
    document.getElementById("chat-message-field").style.width =
      document.querySelector(".meeting-chat .container").offsetWidth + "px";
    let maxHeight =
      document.querySelector("section").offsetHeight -
      document.querySelector(".container-video-call").offsetHeight -
      20 -
      document.getElementById("header-chat").offsetHeight -
      70;
    document.querySelector(".chat-body .message").style.maxHeight =
      maxHeight + "px";
    document.querySelector(".ps").style.height = maxHeight + "px";
  },

  methods: {
    sendMessage: function () {
      var TIME_INTERVAL = 60000;
      var date = new Date();
      if (!this.current_user.user_messages) return;
      this.$refs.messageField.innerHTML = "";
      if (
        this.chat_info.messages[this.chat_info.messages.length - 1].user_id ==
        this.current_user.user_id &&
        date -
        this.chat_info.messages[this.chat_info.messages.length - 1].date <=
        TIME_INTERVAL
      ) {
        this.chat_info.messages[
          this.chat_info.messages.length - 1
        ].user_messages.push(this.current_user.user_messages);
        this.current_user.user_messages = "";
        return;
      }
      this.chat_info.messages.push({
        user_id: this.current_user.user_id,
        user_name: this.current_user.user_name,
        user_img: this.current_user.user_img,
        timestamp:
          new Date().getHours() +
          ":" +
          (new Date().getMinutes() < 10
            ? "0" + new Date().getMinutes()
            : new Date().getMinutes()),
        date: date,
        user_messages: [this.current_user.user_messages],
      });
      this.current_user.user_messages = "";
    },
    getCurrentMessage: function (event) {
      this.current_user.user_messages = event.target.textContent;
    },
    senMessageByEnter: function (event) {
      if (event.keyCode != 13) return;
      this.sendMessage();
      event.preventDefault();
    },
  },
};
</script>

